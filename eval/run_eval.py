CAVEAT = (
    "[Note: circular-reasoning disclosure]\n"
    "  This result is structurally inevitable given the synthetic data. The synthetic data is\n"
    "  generated by adding noise + tourism bias to stars derived from true_food_quality, and the\n"
    "  synthetic labels are also derived from true_food_quality, so it is by design that FQS\n"
    "  (derived from aspect_food) tends to agree with the labels -- this is not a proof of the method.\n"
    "  The real validation is done by running the same pipeline on real labels (Michelin / Repsol listings)."
)


# ---------------------------------------------------------------- independence check
def independence_check(places) -> float:
    """Correlation between the label source (true_food_quality) and FQS. Expect positive but != 1.0 (=1.0 means leakage)."""
    pairs = [(p.true_food_quality, p.fqs) for p in places
             if p.true_food_quality is not None and p.fqs is not None]
    tfq = np.array([a for a, _ in pairs])
    fqs = np.array([b for _, b in pairs])
    r = float(np.corrcoef(tfq, fqs)[0, 1])
    print("\n===== independence check (label source true_food_quality ⊥ FQS) =====")
    print(f"  Pearson(true_food_quality, FQS) = {r:+.3f}  (n={len(pairs)})")
    if r >= 0.999:
        raise SystemExit(
            "  !! LEAK: correlation is nearly 1.0. true_food_quality is leaking into FQS. Aborting.")
    print("  -> positive correlation but not a perfect match (diverging only by star distortion/noise). "
          "The labels are not a copy of FQS, so the evaluation is meaningful.")
    return r


# ---------------------------------------------------------------- display
def _fmt(v) -> str:
    return "  nan" if v != v else f"{v:6.3f}"


def print_scope_table(title: str, res: dict, ks) -> None:
    meta = res["_meta"]
    print(f"\n----- {title} -----")
    extra = (f"  groups_used={meta['groups_used']}/{meta['groups_total']}"
             if "groups_used" in meta else
             f"  n={meta['n']}  listed={meta['n_positive']}")
    print(f"  scope meta:{extra}")
    cols = []
    for k in ks:
        cols += [f"P@{k}", f"R@{k}", f"NDCG@{k}"]
    cols += ["AUC"]
    print("  metric :  " + " ".join(f"{c:>8}" for c in cols))
    for scorer in ("star", "fqs"):
        vals = []
        for k in ks:
            vals += [res[scorer][f"precision@{k}"], res[scorer][f"recall@{k}"],
                     res[scorer][f"ndcg@{k}"]]
        vals += [res[scorer]["auc"]]
        print(f"  {scorer:6} :  " + " ".join(f"{_fmt(v):>8}" for v in vals))


# ---------------------------------------------------------------- fig4: capture rate
def fig_capture_rate(res_global: dict, ks, headline_k, out: str) -> dict:
    star = [res_global["star"][f"recall@{k}"] for k in ks]
    fqs = [res_global["fqs"][f"recall@{k}"] for k in ks]
    x = np.arange(len(ks)); w = 0.38
    fig, ax = plt.subplots(figsize=(8, 5.6))
    b1 = ax.bar(x - w / 2, star, w, label="Google star ranking", color="#4C72B0")
    b2 = ax.bar(x + w / 2, fqs, w, label="FQS ranking", color="#55A868")
    for bars in (b1, b2):
        for r in bars:
            ax.text(r.get_x() + r.get_width() / 2, r.get_height() + 0.01,
                    f"{r.get_height():.0%}", ha="center", fontsize=9)
    ax.set_xticks(x); ax.set_xticklabels([f"top-{k}" for k in ks])
    ax.set_ylabel("capture rate of guide-listed places (Recall@K)")
    ax.set_ylim(0, 1)
    hs, hf = res_global["star"][f"recall@{headline_k}"], res_global["fqs"][f"recall@{headline_k}"]
    ax.set_title("Guide-listed capture rate: Google star vs FQS\n"
                 f"top-{headline_k}: star {hs:.0%}  ->  FQS {hf:.0%}  (synthetic data)")
    ax.legend()
    ax.grid(axis="y", alpha=0.2)
    ax.text(0.5, -0.18, "Synthetic data + synthetic labels — structural, NOT a proof of method. "
            "Validate on real Michelin/Repsol labels.",
            transform=ax.transAxes, ha="center", fontsize=7.5, color="#822")
    fig.tight_layout()
    fig.savefig(out, dpi=130, bbox_inches="tight")
    plt.close(fig)
    return {"headline_star": hs, "headline_fqs": hf}


# ---------------------------------------------------------------- fig5: ablation
def fig_ablation(rows, out: str) -> None:
    names = [r["config"] for r in rows]
    auc = [r["fqs_auc"] for r in rows]
    rec = [r["fqs_recall@10"] for r in rows]
    x = np.arange(len(names)); w = 0.38
    fig, ax = plt.subplots(figsize=(9, 5.4))
    ax.bar(x - w / 2, auc, w, label="FQS AUC", color="#8172B3")
    ax.bar(x + w / 2, rec, w, label="FQS Recall@10", color="#CCB974")
    for i, (a, rc) in enumerate(zip(auc, rec)):
        ax.text(i - w / 2, a + 0.01, f"{a:.3f}", ha="center", fontsize=8)
        ax.text(i + w / 2, rc + 0.01, f"{rc:.2f}", ha="center", fontsize=8)
    ax.set_xticks(x); ax.set_xticklabels(names, rotation=12, fontsize=9)
    ax.set_ylim(0, 1); ax.set_ylabel("metric")
    ax.set_title("Ablation: FQS metrics with each signal removed (synthetic)")
    ax.legend(); ax.grid(axis="y", alpha=0.2)
    fig.tight_layout()
    fig.savefig(out, dpi=130)
    plt.close(fig)


# ---------------------------------------------------------------- main
def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--n-places", type=int, default=120)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--top-frac", type=float, default=0.2, help="top fraction for synthetic labels")
    ap.add_argument("--labels-csv", default=None, help="real-label CSV (used when specified)")
    ap.add_argument("--ks", type=int, nargs="+", default=[5, 10, 20])
    ap.add_argument("--headline-k", type=int, default=10)
    args = ap.parse_args()
    ks = tuple(args.ks)
    os.makedirs(REPORTS_DIR, exist_ok=True)

    # 1) synth -> ABSA(dummy) -> FQS(full weights)
    places, reviews = generate(n_places=args.n_places, seed=args.seed)
    get_analyzer("simple").analyze(reviews)
    base_w = Weights()
    score_places(places, reviews, base_w, now=NOW)

    # 2) proxy labels (synthetic or real CSV)
    if args.labels_csv:
        labels = get_proxy_labels(places, source="csv", csv_path=args.labels_csv)
        src = f"CSV {args.labels_csv}"
    else:
        labels = get_proxy_labels(places, source="synthetic", top_frac=args.top_frac)
        src = f"synthetic top-{args.top_frac:.0%} of true_food_quality"
    print(f"labels: {len(labels)}/{len(places)} listed  ({src})")

    # 3) independence check (only meaningful with synthetic labels)
    if not args.labels_csv:
        independence_check(places)

    # 4) global + within-group evaluation
    both = evaluate_both(places, labels, ks=ks, scope="district_cuisine")
    print("\n===== metrics: star ranking vs FQS ranking =====")
    print_scope_table("GLOBAL (all-places pool)", both["global"], ks)
    print_scope_table("GROUPED (same district x cuisine, macro-average)", both["grouped"], ks)

    # 5) capture-rate comparison (headline) + fig4
    f4 = os.path.join(REPORTS_DIR, "fig4_capture_rate_star_vs_fqs.png")
    cap = fig_capture_rate(both["global"], ks, args.headline_k, f4)
    print(f"\n  >> capture rate of listed places (global Recall@{args.headline_k}): "
          f"star {cap['headline_star']:.0%}  ->  FQS {cap['headline_fqs']:.0%}")

    # 6) ablation (recompute FQS for each config and re-evaluate) + fig5
    print("\n===== ablation (remove each signal one at a time and re-evaluate FQS, global) =====")
    rows = ablation_table(places, reviews, base_w, labels, ks=ks, now=NOW)
    print(f"  {'config':18} {'AUC':>7} {'R@10':>7} {'NDCG@10':>8} {'P@10':>7}")
    for r in rows:
        print(f"  {r['config']:18} {r['fqs_auc']:7.3f} {r['fqs_recall@10']:7.3f} "
              f"{r['fqs_ndcg@10']:8.3f} {r['fqs_precision@10']:7.3f}")
    print("  note: no_reviewer_weight is inert in synth as there is no reviewer history (no difference). "
          "A signal to enable on real data (McAuley).")
    f5 = os.path.join(REPORTS_DIR, "fig5_ablation_fqs.png")
    fig_ablation(rows, f5)
    # restore the FQS overwritten by ablation back to full (safety for downstream use)
    score_places(places, reviews, base_w, now=NOW)

    print("\n" + CAVEAT)
    print(f"\nfigures saved: {os.path.relpath(f4)} , {os.path.relpath(f5)}")


if __name__ == "__main__":
    main()
